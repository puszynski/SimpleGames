//Game Models, Enums

enum MapTileFunctions {
    //reachable
    Grass = 0,
    Path = 1,
    Sand = 2,
    MudOrSwamp = 3,
    ShallowWater = 4,

    //blocked
    DeepWater = 5,
    Stone = 6,
    Tree = 7,
}

class Map {
    public Name: string;
    public MapTiles: number[][];

    constructor(name: string, mapTiles: number[][]) {
        this.Name = name;
        this.MapTiles = mapTiles;
    }

    draw(): void {
        const mapTileCountRows = this.MapTiles.length;
        const mapTileCountCoulmns = this.MapTiles[0].length;

        for (let y = 0; y < mapTileCountRows; y++) {
            for (let x = 0; x < mapTileCountCoulmns; x++) {
                this.drowTile(TILE_SIZE * x, TILE_SIZE * y, this.MapTiles[y][x]);
            }         
        }   
    }

    private drowTile(x: number, y: number, tile: MapTileFunctions): void {
        CTX.beginPath();

        switch (tile) {
            case MapTileFunctions.DeepWater:
                CTX.fillStyle = "blue";
            case MapTileFunctions.Grass:
                CTX.fillStyle = "green";
            default:
                CTX.fillStyle = "black";
        }

        CTX.fillRect(x, y, TILE_SIZE, TILE_SIZE);
    }
}


//Initialize codes, game functions
ACTUALMAP = new Map('SquareEmtyMap', MapTiles_1_40x40);

function gameLoop() {
    requestAnimationFrame(gameLoop);

    //clear all by set black background
    CTX.fillStyle = "black";
    CTX.fillRect(0, 0, CANVAS.width, CANVAS.height)

    //draw map
    ACTUALMAP.draw();
}

